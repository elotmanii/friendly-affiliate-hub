import { useParams, useNavigate } from "react-router-dom";
import { motion } from "framer-motion";
import { Home } from "lucide-react";
import { Button } from "@/components/ui/button";
import ProductImageGallery from "@/components/ProductImageGallery";
import ProductReviews from "@/components/ProductReviews";
import { ScrollArea } from "@/components/ui/scroll-area";
import AffiliateButtons from "@/components/AffiliateButtons";
import ProductHeader from "@/components/ProductHeader";
import ProductFeatures from "@/components/ProductFeatures";
import ProductSpecs from "@/components/ProductSpecs";
import { products } from "../data/products";

const ProductView = () => {
  const { id } = useParams();
  const navigate = useNavigate();

  const product = products.find((p) => p.id === Number(id));

  const socialLinks = {
    telegram: "https://t.me/your_telegram_community",
    facebook: "https://facebook.com/groups/your_facebook_group",
  };

  if (!product) {
    return (
      <div className="fixed inset-0 bg-background/80 backdrop-blur-sm z-50 flex items-center justify-center">
        <div className="bg-white p-6 rounded-lg shadow-lg text-center">
          <h2 className="text-2xl font-bold mb-4 text-amazon-dark">Product Not Found</h2>
          <Button 
            onClick={() => navigate("/")}
            className="bg-amazon-orange hover:bg-amazon-yellow text-black font-semibold"
          >
            Return to Home
          </Button>
        </div>
      </div>
    );
  }

  const discountedPrice = product.discount
    ? product.price * (1 - product.discount / 100)
    : product.price;

  return (
    <motion.div
      initial={{ opacity: 0 }}
      animate={{ opacity: 1 }}
      exit={{ opacity: 0 }}
      className="min-h-screen bg-gradient-to-b from-gray-50 to-white"
    >
      <Button
        variant="ghost"
        size="icon"
        className="fixed right-4 top-4 z-50 bg-white/80 hover:bg-white shadow-md rounded-full lg:right-6 lg:top-6"
        onClick={() => navigate("/")}
      >
        <Home className="h-5 w-5" />
      </Button>

      {/* Desktop Layout */}
      <div className="hidden lg:block min-h-screen">
        <div className="grid lg:grid-cols-[1fr_1.2fr] gap-8 max-w-[2000px] mx-auto">
          <div className="relative min-h-screen bg-gradient-to-b from-gray-50 to-white p-8">
            <div className="sticky top-8 max-w-2xl mx-auto">
              <ProductImageGallery
                images={product.images || [product.image]}
                title={product.title}
              />
            </div>
          </div>

          <ScrollArea className="h-screen">
            <div className="px-8 py-12 max-w-3xl">
              <div className="space-y-10">
                <ProductHeader
                  product={product}
                  discountedPrice={discountedPrice}
                />

                <AffiliateButtons
                  affiliateLinks={product.affiliateLinks}
                  socialLinks={socialLinks}
                  className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100"
                />

                {product.description && (
                  <div className="prose prose-lg max-w-none bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <h2 className="text-2xl font-semibold text-amazon-dark mb-4">
                      Product Description
                    </h2>
                    <p className="text-gray-600 leading-relaxed">
                      {product.description}
                    </p>
                  </div>
                )}

                {product.features && (
                  <div className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <ProductFeatures features={product.features} />
                  </div>
                )}

                {product.specs && (
                  <div className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <ProductSpecs specs={product.specs} />
                  </div>
                )}

                {product.reviews && product.reviews.length > 0 && (
                  <div className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <ProductReviews reviews={product.reviews} />
                  </div>
                )}
              </div>
            </div>
          </ScrollArea>
        </div>
      </div>

      {/* Mobile Layout */}
      <div className="lg:hidden min-h-screen bg-gradient-to-b from-gray-50 to-white">
        <ScrollArea className="h-screen">
          <div className="pb-8 px-4 pt-16 max-w-2xl mx-auto">
            <div className="space-y-8">
              <ProductImageGallery
                images={product.images || [product.image]}
                title={product.title}
              />

              <div className="space-y-8">
                <ProductHeader
                  product={product}
                  discountedPrice={discountedPrice}
                />

                <AffiliateButtons
                  affiliateLinks={product.affiliateLinks}
                  socialLinks={socialLinks}
                  className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100"
                />

                {product.description && (
                  <div className="prose prose-sm max-w-none bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <h2 className="text-xl font-semibold text-amazon-dark mb-3">
                      Product Description
                    </h2>
                    <p className="text-gray-600 leading-relaxed">
                      {product.description}
                    </p>
                  </div>
                )}

                {product.features && (
                  <div className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <ProductFeatures features={product.features} />
                  </div>
                )}

                {product.specs && (
                  <div className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <ProductSpecs specs={product.specs} />
                  </div>
                )}

                {product.reviews && product.reviews.length > 0 && (
                  <div className="bg-white p-6 rounded-2xl shadow-lg border border-gray-100">
                    <ProductReviews reviews={product.reviews} />
                  </div>
                )}
              </div>
            </div>
          </div>
        </ScrollArea>
      </div>
    </motion.div>
  );
};

export default ProductView;